f"""
[ S ]<-------[ R ]
     \     /
      [ G ]

R = true -> S = false
"""

P = {}

P['R'] = 0.2
P['S|R'] = 0.01
P['S|-R'] = 0.4
P['G|S^R'] = 0.99
P['G|S'] = 0.9
P['G|R'] = 0.8

P['G'] = 0.0

P['-R'] = 1 - P['R']
P['S^R'] = P['S|R'] * P['R']
P['S^-R'] = P['S|-R'] * P['-R']


P['G'] = P['G|S^R'] * P['S^R'] \
    + P['G|R^-S'] * P['R^-S'] \
        + P['G|-R^-S'] * P['-R^-S']